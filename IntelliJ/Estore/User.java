public class User {

	private String name;

	private String email;

	private int age;

	private String password;

	private String phone;



	public User(String name, int age, String email, String password, String phone) {
		this.name = name;
		
		this.age = age;
		
		this.email = email;
		
		this.password = password;
		
		this.phone = phone;
	}
}